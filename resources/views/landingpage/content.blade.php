<script src="https://cdnjs.cloudflare.com/ajax/libs/bodymovin/5.9.6/lottie.min.js"></script>
<script>
    const animations = [
        'assets/animations/animal-lottie.json',
        'assets/animations/animal.json',
        'assets/animations/cars-lottie.json',
        'assets/animations/tech-lottie.json',
    ];

    let currentAnimationIndex = 0;
    let animationInstance = null;
    const loader = document.getElementById('loader');

    function loadAnimation() {
        const animationContainer = document.getElementById('lottie-animation');

        // Show loader and fade out animation
        loader.classList.remove('opacity-0');
        animationContainer.style.opacity = 0;

        setTimeout(() => {
            if (animationInstance) {
                animationInstance.destroy();
            }

            // Load new animation
            animationInstance = lottie.loadAnimation({
                container: animationContainer,
                renderer: 'svg',
                loop: true,
                autoplay: true,
                path: animations[currentAnimationIndex],
            });

            // Fade in new animation and hide loader
            animationContainer.style.opacity = 1;
            loader.classList.add('opacity-0');
        }, 500); // Match this duration with the CSS transition duration

        currentAnimationIndex = (currentAnimationIndex + 1) % animations.length;
        setTimeout(loadAnimation, 5000);
    }

    document.getElementById('animation-link').addEventListener('click', (event) => {
        event.preventDefault();
    });

    loadAnimation();
</script>
